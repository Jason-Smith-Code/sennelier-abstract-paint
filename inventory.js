const printinventory = () => {
    var listItems = inventory.map(function(item){

        const displayOpacity = () => {
            if (item.opacity == 1) {
                return '<div class="opacity-row"><div class="square transparent"></div></div>'
            } else if (item.opacity == 2) {
                return '<div class="opacity-row"><div class="square semi-transparent"></div></div>'
            } else if (item.opacity == 3) {
                return '<div class="opacity-row"><div class="square opaque"></div></div>'
            } else if (item.opacity < 1 || item.opacity > 3 ) {
                return '<p>Incorrect opacity value</p>'  
            }
        }

        const displayLightfastness = () => {
            if (item.lightfast == 1) {
                return  '<div class="circle-container">' +
                            '<div class="circle opaque"></div>' +
                            '<div class="circle"></div>'+
                            '<div class="circle"></div>'+
                        '</div>'
            } else if (item.lightfast == 2) {
                return  '<div class="circle-container">' +
                            '<div class="circle opaque"></div>' +
                            '<div class="circle opaque"></div>'+
                            '<div class="circle"></div>'+
                        '</div>'
            } else if (item.lightfast == 3) {
                return  '<div class="circle-container">' +
                            '<div class="circle opaque"></div>' +
                            '<div class="circle opaque"></div>'+
                            '<div class="circle opaque"></div>'+
                        '</div>'
            } else if (item.lightfast < 1 || item.lightfast > 3 ) {
                return '<p>Incorrect opacity value</p>'  
            }
        }

        return  '<div class="item">' +
                    '<div class="image-container">' +
                        '<img class="product-image" src=' + item.image +'>'+
                    '</div>'+
                    '<h3>'  + item.title + '</h3>' +
                    '<p class="product-title">[' + item.code + ']</p>' +
                    '<div class="product-row">' +
                        displayOpacity() + 
                        displayLightfastness() +
                    '</div>'+
                '</div>';
    })
   document.querySelector('#app').innerHTML = listItems.join('');
}

printinventory();